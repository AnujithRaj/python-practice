# Creating a List
lis1 = [62, 87, 57, 71, 88]
print("Original List:", lis1)

# Using append()
lis1.append(95)
print("After Append:", lis1)

# Using insert()
lis1.insert(1, 67)
print("After Insert:", lis1)

# Using Remove()
lis1.remove(57)
print("After Remove:", lis1)

# Using pop()
lis1.pop()
print("After Pop:", lis1)
