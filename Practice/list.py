# Creating a list
lis1 = [19, 31, 93, 53, 73]
print(lis1)

# Add a new element at the end
lis1.append(41)
print(lis1)

# Remove an element.
lis1.remove(31)
print(lis1)

# Find max and Min value
print("Maximum Value: ", max(lis1))
print("Minimum Value: ", min(lis1))

# Reverse the List.
lis1.reverse()
print("Reverse List: ", lis1)
